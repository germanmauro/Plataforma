    10. POLITICA DE DEVOLUCION
</p>
<p>
    Según establece la normativa, el consumidor y usuario tendrá derecho a DESISTIR del contrato durante un 
    período máximo de 14 días naturales sin necesidad de indicar el motivo y sin incurrir en ningún costo, 
    salvo los previstos en el art. 107.2 y 108 del RD1/2007, de 16 de noviembre, por el que se aprueba el 
    texto referido de la Ley General para la Defensa de los Consumidores y Usuarios.
</p>

<p>
    - Excepción:
</p>

<p>
    Según establece el Artículo 103.a) de la citada norma, el derecho de desistimiento no será aplicable a la 
    prestación de servicios cuando su ejecución haya comenzado o se haya ejecutado por completo.
</p>

<p>
    Por tanto, una vez iniciado el curso adquirido, ya no podrá ejercerse el derecho de desistimiento.
</p>

<p>
    Si el curso no se ha iniciado, el cliente dispondrá de un plazo máximo de 14 días naturales desde el día 
    de la inscripción, para informar a CEE (Capacitación en español) sobre su deseo de desistir del contrato, 
    siempre que el curso no se inicie durante ese plazo.
</p>

<p>
    El desistimiento implica que, CEE (Capacitación en español) procederá a la devolución del importe ya 
    abonado por el cliente en un plazo máximo de 14 días naturales, siguiendo el mismo procedimiento elegido 
    por el cliente para su abono.
</p>

<p>
    Durante este período, el consumidor comunicará al empresario su decisión de desistir del contrato rellenando 
    para ello el formulario que se encuentra al final de este documento y enviándolo a la dirección de correo 
    electrónico de CEE (Capacitación en español).
</p>

<p>
    CEE (Capacitación en español) comunicará al consumidor en soporte duradero un acuse de recibo de dicho 
    desistimiento.
</p>

<p>
    Para proceder a la devolución de un producto es imprescindible que el mismo se encuentre en perfecto estado 
    y con su correspondiente envoltorio original, con todos sus accesorios y sin haber sido utilizado.
</p>

<p>
